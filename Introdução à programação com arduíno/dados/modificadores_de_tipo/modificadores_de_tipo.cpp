//Modificadores de tipo tem o objetivo de alterar o comportamento de um tipo existente

//modificador SIGNED, indica que uma variável pode armazenar valores positivos e negativos, pode ser aplicado em INT e CHAR

signed int num1 = 10;
signed char abc = -1;

//modificador UNSIGNED, indica que uma variável só pode armazenar valores positivos, pode ser aplicado em INT e CHAR

unsigned int num1 = 10;
unsigned char abc = 1;

//modificador SHORT, diminui o tamanho uma variável do tipo INT, diminuindo o escopo de valores que a variável pode representar

short int num2 = 10;

//modificador LONG, aumenta o tamanho de uma variável do tipo INT ou DOUBLE, aumentando o escopo de valores que a variável pode representar

long int num3 = 1000;
long double num4 = 1000.000;